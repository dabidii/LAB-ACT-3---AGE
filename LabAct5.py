age = int(input("How old are you?"))
if age >= 100:
	print("Ho is you ancient?! or ...")
elif age >= 65:
	print("You're a Senior, Long Live!.")
elif age >= 20:
	print("You are an adult, good luck!.")
elif age >= 13:
	print("You are a teenager, enjoy life.")
elif age >= 0:
	print("You are a child, hope your childhood will be good.")
else:
	print("Invalid Age")
